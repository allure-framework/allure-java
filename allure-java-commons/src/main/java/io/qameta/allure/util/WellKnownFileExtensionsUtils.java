    private static final Map<String, String> EXTENSIONS_BY_TYPE = createExtensionsByType();

    private WellKnownFileExtensionsUtils() {
        throw new IllegalStateException("Do not instance");
    }

    /**
     * Returns a well-known file extension for the given content type.
     *
     * @param contentType the content type to resolve
     * @return file extension with a leading dot, or an empty string when the content type is unknown
     */
    public static String getExtensionByMimeType(final String contentType) {
        if (Objects.isNull(contentType) || contentType.isEmpty()) {
            return "";
        }
        final String normalizedType = normalizeContentType(contentType);
        final String extension = EXTENSIONS_BY_TYPE.get(normalizedType);
        if (Objects.nonNull(extension)) {
            return extension;
        }
        return getExtensionByBaseMimeType(normalizedType);
    }

