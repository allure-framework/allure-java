    private static String normalizeContentType(final String contentType) {
        return contentType.trim().toLowerCase(Locale.ROOT);
    }

    private static String getExtensionByBaseMimeType(final String contentType) {
        final int optionsSeparator = contentType.indexOf(';');
        if (optionsSeparator < 0) {
            return "";
        }
        return EXTENSIONS_BY_TYPE.getOrDefault(contentType.substring(0, optionsSeparator).trim(), "");
    }

